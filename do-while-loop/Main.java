
/**
 * Main
 */
public class Main {
    public static void main(String args[]) {
        // do while loop akan tetap jalan sekali walaupun kondisinya false
        int i = 10;
        do {
            System.out.print(i);
            i++;
        } while (i < 10);

        System.out.println("\nend");
    }
}